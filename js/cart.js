const itemSelect1 = document.getElementById('btn-1');
itemSelect1.addEventListener('click',function(){
    const selecteditems = document.getElementById('name-1');
    const selecteditemsPrice = document.getElementById('p-1');
    const show = selecteditems.innerText;
    const showPrice = selecteditemsPrice.innerText
    const itemselection = document.getElementById('item-1');
    const priceSelection = document.getElementById('item-price-1');
    itemselection.innerText = show;
    priceSelection.innerText =showPrice;
    document.getElementById('btn-1').disabled = true;
})

